    void mostrartabla(String valor){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id:");
        modelo.addColumn("Pelicula:");
        modelo.addColumn("Sala:");
        modelo.addColumn("Inicia:");
        modelo.addColumn("Termina:");
        modelo.addColumn("Fecha:");
        
        tabla.setModel(modelo);
        String sql="SELECT * FROM Screenings WHERE room_id LIKE '%"+valor+"%'";
        
        String datos[] = new String[6];
        
        Statement st;
        
        try {
            st=cn.createStatement();
            
            ResultSet rs=st.executeQuery(sql);
            
            while(rs.next()){
                obtenerNombreP(rs.getString(2));
                obtenerNombreS(rs.getString(3));
                datos[0]=rs.getString(1);
                datos[1]=cargo.getNombrePelicula();
                datos[2]=cargo.getNombreSala();
                datos[3]=formatoHora(rs.getString(4));
                datos[4]=formatoHora(rs.getString(5));
                datos[5]=rs.getString(6);
                
                modelo.addRow(datos);
            }
            
            
            tabla.setModel(modelo);
            
        } catch (SQLException e) {
            
            System.err.println("Error en el llenado de la tabla...");
            
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla...");
        }
    }

