    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        fecha();
        obteneridP();
        obteneridS();

        try {
            
            String horaInicialStr = txtIni.getText();
            LocalTime horaInicial = LocalTime.parse(horaInicialStr);
            String query = "SELECT duration FROM Movies WHERE movie_id = ?";
            int movieId = Integer.parseInt(cargo.getIdPelicula());
            PreparedStatement statement = cn.prepareStatement(query);
            statement.setInt(1, movieId);
            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                int duracion = resultSet.getInt("duration");
                LocalTime horaFinal = horaInicial.plusMinutes(duracion);
                String horaFinalStr = horaFinal.toString();
                PreparedStatement ps = cn.prepareStatement("EXEC InsertScreening "
                                                            + "@p_movie_id = ?, "
                                                            + "@p_room_id = ?, "
                                                            + "@p_start_time = ?, "
                                                            + "@p_end_time = ?, "
                                                            + "@p_screening_date = ?");

                ps.setInt(1, Integer.parseInt(cargo.getIdPelicula()));
                ps.setInt(2, Integer.parseInt(cargo.getIdSala()));
                ps.setString(3, horaInicialStr); 
                ps.setString(4, horaFinalStr); 
                ps.setString(5, formattedDate); 
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos guardados");
                mostrartabla("");
                limpiar();
            } else {
                System.out.println("No se encontró la duración de la película con el ID especificado.");
            }
        } catch (SQLException e) { 
            System.err.println("Error en el llenado de la tabla: " + e);
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");
        }


        
        
    }//GEN-LAST:event_btnguardarActionPerformed

