    private void TotalPagar(){
        totalPagar = 0.00;
        
        int numFila= Tabla.getRowCount();
        for (int i = 0; i < numFila; i++) {
            double cal = Double.parseDouble(String.valueOf(Tabla.getModel().getValueAt(i, 4)));
            totalPagar= totalPagar + cal;
            
        }
        lbTotal.setText(String.format("%.2f", totalPagar));
    }
    private void limpiarVenta(){
        txtCodigo.setText("");
        txtProducto.setText("");
        txtCantidad.setText("");
        txtStock.setText("");
        txtPrecio.setText("");
    }
    private void RegistarVenta(){
        double total = totalPagar;
        v.setTotal(total);
        vDao.registrarVenta(v);
    }
    
    private void RegistrarDetalle(){
        int id =vDao.idVenta();
        for (int i = 0; i < Tabla.getRowCount(); i++) {
            int cod = Integer.parseInt(Tabla.getValueAt(i, 0).toString());
            int cant = Integer.parseInt(Tabla.getValueAt(i, 2).toString());
            double precio = Double.parseDouble(Tabla.getValueAt(i, 3).toString());
            dv.setProductoid(cod);
            dv.setCantidad(cant);
            dv.setPreciouni(precio);
            dv.setVentaid(id);
            vDao.RegistroDetalle(dv);
        }
    }
    private void actualizarStock(){
        for (int i = 0; i < Tabla.getRowCount(); i++) {
            
            String cod = Tabla.getValueAt(i, 0).toString();
            int cant = Integer.parseInt(Tabla.getValueAt(i, 2).toString());
            pro = proDao.buscarProd(cod);
           
            int StockActual = pro.getStock()-cant;
            
            vDao.ActualizarStock(StockActual, cod);
            
        }
    }

