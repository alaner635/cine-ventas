    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txtCodigo.getText())) {
                String cod=txtCodigo.getText();
                pro = proDao.buscarProd(cod);
                if (pro.getNombre()!= null ) {
                    txtProducto.setText(""+pro.getNombre());
                    txtPrecio.setText(""+pro.getPrecio());
                    txtStock.setText(""+pro.getStock());
                    txtCantidad.requestFocus();
                }else{
                    limpiarVenta();
                    txtCodigo.requestFocus();
                
                }
            }else{
                JOptionPane.showMessageDialog(null, "Ingresar el codigo del producto");
                txtCodigo.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoKeyPressed

    private void txtCantidadKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCantidadKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!"".equals(txtCantidad.getText())){
                String cod= txtCodigo.getText();
                String producto= txtProducto.getText();
                int cant= Integer.parseInt(txtCantidad.getText());
                double precio = Double.parseDouble(txtPrecio.getText());
                double total = cant * precio;
                int Stock=Integer.parseInt(txtStock.getText());
                if (Stock >= cant){
                    item=item+1;
                    modelo = (DefaultTableModel) Tabla.getModel();
                    for (int i = 0; i < Tabla.getRowCount(); i++) {
                        if( Tabla.getValueAt(i, 1).equals(txtProducto.getText())){
                            JOptionPane.showMessageDialog(null, "El producto ya esta registrado.");
                            return;
                        }
                    }
                    ArrayList lista = new ArrayList();
                    lista.add(item);
                    lista.add(cod);
                    lista.add(producto);
                    lista.add(cant);
                    lista.add(precio);
                    lista.add(total);
                    Object[] O = new Object[5];

                    O[0]=lista.get(1);
                    O[1]=lista.get(2);
                    O[2]=lista.get(3);
                    O[3]=lista.get(4);
                    O[4]=lista.get(5);
                    modelo.addRow(O);
                    Tabla.setModel(modelo);
                    TotalPagar();
                    limpiarVenta();
                    txtCodigo.requestFocus();
                }else{
                    JOptionPane.showMessageDialog(null, "Stock no disponible.");
                }
            
            }else{
                JOptionPane.showMessageDialog(null, "Ingrese cantidad.");
            }

        }   
    }//GEN-LAST:event_txtCantidadKeyPressed

