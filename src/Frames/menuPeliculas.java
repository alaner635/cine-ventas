    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        fecha();
        combo();
        try {
            // Preparar la consulta SQL con los parámetros del procedimiento almacenado
            PreparedStatement ps = cn.prepareStatement("EXEC InsertMovie "
                                                        + "@p_title = ?, "
                                                        + "@p_director = ?, "
                                                        + "@p_genre = ?, "
                                                        + "@p_duration = ?, "
                                                        + "@p_release_date = ?, "
                                                        + "@p_estado = ?");
    
            ps.setString(1, txtTitulo.getText());
            ps.setString(2, txtDirector.getText());
            ps.setString(3, txtGenero.getText());
            ps.setInt(4, Integer.parseInt(txtDuracion.getText()));
            ps.setString(5, formattedDate);
            ps.setInt(6, select); // Asegúrate de obtener el valor correcto para el estado

            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Datos guardados.");

            limpiar();
            mostrartabla("");

        } catch (SQLException e) { 
            System.err.println("Error al insertar película: " + e);
            JOptionPane.showMessageDialog(null, "Error al insertar película.");
        }

        
        
    }//GEN-LAST:event_btnguardarActionPerformed

