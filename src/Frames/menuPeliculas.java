    private void btnactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnactualizarActionPerformed

        fecha();
        combo();
        try {
            PreparedStatement ps = cn.prepareStatement("EXEC UpdateMovie "
                                                        + "@p_movie_id = ?, "
                                                        + "@p_title = ?, "
                                                        + "@p_director = ?, "
                                                        + "@p_genre = ?, "
                                                        + "@p_duration = ?, "
                                                        + "@p_release_date = ?, "
                                                        + "@p_estado = ?");

            ps.setInt(1, Integer.parseInt(txtid.getText())); 
            ps.setString(2, txtTitulo.getText()); 
            ps.setString(3, txtDirector.getText()); 
            ps.setString(4, txtGenero.getText()); 
            ps.setInt(5, Integer.parseInt(txtDuracion.getText())); 
            ps.setString(6, formattedDate);
            ps.setInt(7, select);

            int respuesta = ps.executeUpdate();

            if (respuesta > 0) {
                JOptionPane.showMessageDialog(null, "Datos actualizados.");
                limpiar();
                mostrartabla("");
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado una fila.");
            }
        } catch (SQLException e) {
            System.err.println("Error al actualizar: " + e);
            JOptionPane.showMessageDialog(null, "Error al actualizar.");
        }

        
    }//GEN-LAST:event_btnactualizarActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked

        int fila=this.tabla.getSelectedRow();
        
        this.txtid.setText(this.tabla.getValueAt(fila, 0).toString());
        this.txtTitulo.setText(this.tabla.getValueAt(fila, 1).toString());
        this.txtDirector.setText(this.tabla.getValueAt(fila, 2).toString());
        this.txtGenero.setText(this.tabla.getValueAt(fila, 3).toString());
        this.txtDuracion.setText(this.tabla.getValueAt(fila, 4).toString());
        
        String fechaString = this.tabla.getValueAt(fila, 5).toString();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date fechaDate = sdf.parse(fechaString);
            this.dateC.setDate(fechaDate);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_tablaMouseClicked

