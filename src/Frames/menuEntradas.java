    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        obteneridC();
        try {
                PreparedStatement ps = cn.prepareStatement("{CALL inserentrada(?, ?, ?, ?, ?, ?)}");

                ps.setString(1, cargo.getIdCliente());
                ps.setString(2, (String) cbxListaP.getSelectedItem());
                ps.setString(3, (String) cbxListaS.getSelectedItem()); 
                ps.setString(4, (String) cbxListaF.getSelectedItem()); 
                ps.setString(5, (String) cbxListaH.getSelectedItem()); 
                ps.setDouble(6, Double.parseDouble(txtprecio.getText()));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos guardados");
                mostrartabla("");
                limpiar();
        } catch (SQLException e) { 
            System.err.println("Error en el llenado de la tabla: " + e);
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla");
        }


        
        
    }//GEN-LAST:event_btnguardarActionPerformed

