    void mostrartabla(String valor){
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Id:");
        modelo.addColumn("Cliente:");
        modelo.addColumn("pelicula:");
        modelo.addColumn("sala:");
        modelo.addColumn("fecha:");
        modelo.addColumn("hora:");
        modelo.addColumn("precio:");
        
        tabla.setModel(modelo);
        String sql="SELECT * FROM Entradas WHERE pelicula LIKE '%"+valor+"%'";
        
        String datos[] = new String[7];
        
        Statement st;
        
        try {
            st=cn.createStatement();
            
            ResultSet rs=st.executeQuery(sql);
            
            while(rs.next()){
                obtenerNombreC(rs.getString(2));
                datos[0]=rs.getString(1);
                datos[1]=cargo.getNombreCliente();
                datos[2]=rs.getString(3);
                datos[3]=rs.getString(4);
                datos[4]=rs.getString(5);
                datos[5]=rs.getString(6);
                datos[6]=rs.getString(7);
                
                modelo.addRow(datos);
            }
            
            
            tabla.setModel(modelo);
            
        } catch (SQLException e) {
            
            System.err.println("Error en el llenado de la tabla...");
            
            JOptionPane.showMessageDialog(null, "Error en el llenado de la tabla...");
        }
    }

